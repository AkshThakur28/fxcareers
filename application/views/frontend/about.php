<script>
    const imgWraps = document.querySelectorAll('.img_wrap');
    let currentIndex4 = 0;

    function updateCarousel() {
        imgWraps.forEach((imgWrap, index) => {
            if (index === currentIndex4) {
                imgWrap.style.flex = '3'; // Increase width for the active item
            } else {
                imgWrap.style.flex = '1'; // Reset width for other items
            }
        });

        // Move to the next item
        currentIndex4 = (currentIndex4 + 1) % imgWraps.length;
    }

    // Automatically update the carousel every 3 seconds
    setInterval(updateCarousel, 3000);

    // Initial call to set the first item as active
    updateCarousel();
</script>
