<script>
    document.querySelectorAll('.apply-btn').forEach(function (button) {
        button.addEventListener('click', function () {
            var profileId = this.getAttribute('data-profile-id');
            document.getElementById('profile').value = profileId;
        });
    });

    document.getElementById('uploadForm').addEventListener('submit', function (event) {
        var upload = document.getElementById('upload');
        var filePath = upload.value;
        var fileExtension = filePath.split('.').pop().toLowerCase();

        if (fileExtension !== 'pdf') {
            alert('Error: Only PDF files are allowed!');
            event.preventDefault();
        }
    });
</script>