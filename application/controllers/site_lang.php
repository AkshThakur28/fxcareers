<script>
   function googleTranslateElementInit() {
      new google.translate.TranslateElement({pageLanguage: 'en'}, 'google_translate_element');
   }

   function translatePage(lang) {
      var select = document.querySelector(".goog-te-combo");
      if (select) {
         select.value = lang;
         select.dispatchEvent(new Event("change"));
         document.getElementById("currentLang").innerText = lang === 'fa' ? 'Farsi' : 'English';

         // Store selection in localStorage to persist language
         localStorage.setItem('selectedLang', lang);
      }
   }

   // Auto-apply language selection on page load
   window.onload = function () {
      var storedLang = localStorage.getItem('selectedLang');
      if (storedLang) {
         translatePage(storedLang);
      }
   };
</script>

